package com.module.gson.bean;

public class FieldBean {

	private String fieldDisplay;
	
	private String objClassName;
	
	private String objFields;

	

	public String getFieldDisplay() {
		return fieldDisplay;
	}

	public void setFieldDisplay(String fieldDisplay) {
		this.fieldDisplay = fieldDisplay;
	}

	public String getObjClassName() {
		return objClassName;
	}

	public void setObjClassName(String objClassName) {
		this.objClassName = objClassName;
	}

	public String getObjFields() {
		return objFields;
	}

	public void setObjFields(String objFields) {
		this.objFields = objFields;
	}
	
	
}
